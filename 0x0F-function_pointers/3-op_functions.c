/**
 * op_add - Function that adds two integers
 * @a: First num
 * @b: Second num
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function that subtracts two integers
 * @a: First num
 * @b: Second num
 * Return: Difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function that multiplies two integers
 * @a: First num
 * @b: Second num
 * Return: Product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function that divides two integers
 * @a: First num
 * @b: Second num
 * Return: Product
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Fucntion that modulates two integers
 * @a: First num
 * @b: Second num
 * Return: Remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
